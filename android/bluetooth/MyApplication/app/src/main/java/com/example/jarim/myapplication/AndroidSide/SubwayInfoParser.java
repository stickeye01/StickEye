    /**
     * find the shortest landmark
     * @param lat
     * @param lng
     * @return
     */
    public SubwayInfo findShortestLandmark(double lat, double lng) {
        if (subLists.size() > 0) {
            int shortestIndex = -1;
            double shortestDistance = 999999999f;
            double tmp = 0.0f;
            int idx = 0;
            // Shortest
            for (SubwayInfo subway : subLists) {
                tmp = distance(subway.getLat(), subway.getLng(), lat, lng, 'K');
                if (tmp <= shortestDistance) {
                    shortestIndex = idx;
                    shortestDistance = tmp;
                }
                idx ++;
            }

            return subLists.get(shortestIndex);
        } else {
            return null;
        }
    }

    /**
     * calculate the distance between lat/longitude
     * @param lat1
     * @param lon1
     * @param lat2
     * @param lon2
     * @param unit
     * @return
     */
    private double distance(double lat1, double lon1, double lat2, double lon2, char unit) {
        double theta = lon1 - lon2;
        double dist = Math.sin(deg2rad(lat1)) * Math.sin(deg2rad(lat2)) +
                Math.cos(deg2rad(lat1)) * Math.cos(deg2rad(lat2)) * Math.cos(deg2rad(theta));
        dist = Math.acos(dist);
        dist = rad2deg(dist);
        dist = dist * 60 * 1.1515;
        if (unit == 'K') { // KM
            dist = dist * 1.609344;
        } else if (unit == 'N') { // Mile
            dist = dist * 0.8684;
        }
        return (dist);
    }

    /**
     * converts decimal degrees to radians
     * @param deg
     * @return
     */
    private double deg2rad(double deg) {
        return (deg * Math.PI / 180.0);
    }

    /**
     * converts radians to decimal degrees
     * @param rad
     * @return
     */
    private double rad2deg(double rad) {
        return (rad * 180.0 / Math.PI);
    }
